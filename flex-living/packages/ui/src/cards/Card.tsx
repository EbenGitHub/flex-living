"use client"

import * as React from "react";
import { cn } from "@flex-living/utils/react";

// Base Card
const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-lg border border-gray-200 bg-white text-gray-900 shadow-md transition-shadow hover:shadow-lg",
        className
      )}
      {...props}
    />
  )
);
Card.displayName = "Card";

